# Explain the difference between the = operator and the == operator in Python.
# = operator
"""
= (Assignment Operator): This operator is used to assign a value to a variable.
For example, x = 10 assigns the value 10 to the variable x


"""
# == operator
"""
== (Equality Operator): This operator is used to compare two values for equality.
It checks whether the values on both sides of the operator are equal and returns True if they are,
 or False if they are not. For example, x == 10 checks if x is equal to 10."""

