# What does the ^ operator do in Python, and in what context is it commonly used?
"""
^ operator is a bitwise XOR (exclusive OR) operator. 
It performs a comparison of two numbers at the binary level, 
returning a new number where each bit is set to 1 if the corresponding bits of the operands are different, 
and 0 if they are the same.
"""
a = 5  # In binary: 0101
b = 3  # In binary: 0011
result = a ^ b  # Result in binary: 0110, which is 6 in decimal

print(result)  # Output: 6

